package com.press.comm.pojo;

public class PicInfo {
	private String picid;
	private String relid;
	private String picnam;
	private String picremark;

	public String getPicid() {
		return picid;
	}

	public void setPicid(String picid) {
		this.picid = picid;
	}

	public String getRelid() {
		return relid;
	}

	public void setRelid(String relid) {
		this.relid = relid;
	}

	public String getPicnam() {
		return picnam;
	}

	public void setPicnam(String picnam) {
		this.picnam = picnam;
	}

	public String getPicremark() {
		return picremark;
	}

	public void setPicremark(String picremark) {
		this.picremark = picremark;
	}

	

}
